// Getting True  at Least 6 Times From Two Variables
// The Two Variables
let num1 = 10;
let num2 = 20;

// First Time
console.log(!num1 === !num2); //True

// Second Time
console.log(num1 !== num2); //True

// Third Time
console.log(num1 != num2); //True

// Fourth Time
console.log(num1 < num2); //True

// Fifth Time
console.log(num2 >= num1); //True

// Sixth Time
console.log(num2 > num1); //True
