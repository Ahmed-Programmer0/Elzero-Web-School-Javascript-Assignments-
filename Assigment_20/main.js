// Getting 10 by Five different Ways
let flt = 10.4;

// Solution One
console.log(Math.trunc(flt));

// Solution Two
console.log(Math.floor(flt));

// Solution Three
console.log(Math.round(flt));

// Solution Four
console.log(Number(flt.toFixed(0)));

// Solution Five
console.log(Number.parseInt(flt));
